#include<iostream>
using namespace std;

int main(){
    int A = 4 , B = 6;
    cout<<"A & B ->"<<(A&B)<<endl;
    cout<<"A | B ->"<<(A|B)<<endl;
    cout<<"~A ->"<<(~A)<<endl;
    cout<<"A ^ B ->"<<(A ^ B)<<endl;

    //Leftshift operator

    cout<<" 17 >> 1 ->"<<(17>>1)<<endl;
    cout<<" 17 >> 2 ->"<<(17>>2)<<endl;

    //Leftshift operator

    cout<<" 5 << 1 ->"<<(5<<1)<<endl;
    cout<<" 19 << 2 ->"<<(21<<2)<<endl;
}